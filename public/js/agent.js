/**
 * Fitness Buddy — Agent Module
 * Handles PLAN → EXECUTE → REFLECT agentic loop on the frontend
 * localStorage key: fb_weekly_plan
 */

'use strict';

// ── State ──────────────────────────────────────────────────────────────────
const AgentState = {
  weeklyPlan: null,
  lastReflection: null,
  isLoading: false,
};

// ── Storage helpers ────────────────────────────────────────────────────────
function saveWeeklyPlan(plan) {
  try {
    localStorage.setItem('fb_weekly_plan', JSON.stringify(plan));
    AgentState.weeklyPlan = plan;
  } catch (_) {}
}

function loadWeeklyPlan() {
  try {
    const raw = localStorage.getItem('fb_weekly_plan');
    if (raw) AgentState.weeklyPlan = JSON.parse(raw);
  } catch (_) {}
  return AgentState.weeklyPlan;
}

function saveReflection(reflection) {
  try {
    localStorage.setItem('fb_last_reflection', JSON.stringify(reflection));
    AgentState.lastReflection = reflection;
  } catch (_) {}
}

function loadReflection() {
  try {
    const raw = localStorage.getItem('fb_last_reflection');
    if (raw) AgentState.lastReflection = JSON.parse(raw);
  } catch (_) {}
  return AgentState.lastReflection;
}

// Build activity history from existing localStorage data for the reflector
function buildHistory() {
  const history = [];
  try {
    // Workout history
    const workouts = JSON.parse(localStorage.getItem('fb_workouts') || '[]');
    const weekAgo = Date.now() - 7 * 24 * 60 * 60 * 1000;
    workouts.filter(w => w.timestamp > weekAgo).forEach(w => {
      history.push({ type: 'workout', calories: w.calories, date: w.date, duration: w.duration, workout_type: w.type });
    });

    // Calorie history
    const calData = JSON.parse(localStorage.getItem('fb_calories') || '{}');
    Object.entries(calData).forEach(([date, entries]) => {
      const total = entries.reduce((s, e) => s + (e.calories || 0), 0);
      if (total > 0) history.push({ type: 'food', calories: total, date });
    });
  } catch (_) {}
  return history;
}

// ── API Calls ──────────────────────────────────────────────────────────────
async function fetchAgentPlan(profile, goal) {
  const res = await fetch('/api/agent/plan', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ profile, goal }),
  });
  if (!res.ok) throw new Error(`Plan API ${res.status}`);
  return res.json();
}

async function fetchAgentReflect(weeklyPlan, history) {
  const res = await fetch('/api/agent/reflect', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ weeklyPlan, history }),
  });
  if (!res.ok) throw new Error(`Reflect API ${res.status}`);
  return res.json();
}

async function fetchAgentExecute(task, context = {}) {
  const res = await fetch('/api/agent/execute', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ mode: 'execute', task, context }),
  });
  if (!res.ok) throw new Error(`Execute API ${res.status}`);
  return res.json();
}

// ── Plan Generation ────────────────────────────────────────────────────────
async function generateWeeklyPlan(forceNew = false) {
  if (AgentState.isLoading) return;
  if (!forceNew && AgentState.weeklyPlan) {
    renderWeeklyPlan(AgentState.weeklyPlan);
    return;
  }

  AgentState.isLoading = true;
  setAgentBadge('loading');
  renderPlanSkeleton();

  try {
    const profile = typeof getProfile === 'function' ? getProfile() : {};
    const goal = profile.goals || 'general fitness';

    const data = await fetchAgentPlan(profile, goal);

    if (data.success && data.weekly_plan) {
      saveWeeklyPlan(data);
      renderWeeklyPlan(data);
      if (typeof showToast === 'function') showToast('🤖 7-day plan generated by Buddy AI!', 'success');
      setAgentBadge('online');
    } else {
      throw new Error('Invalid plan response');
    }
  } catch (err) {
    console.error('[Agent] Plan generation failed:', err.message);
    if (typeof showToast === 'function') showToast('Plan generation failed — using smart defaults.', 'error');
    setAgentBadge('offline');
    renderNoPlan();
  } finally {
    AgentState.isLoading = false;
  }
}

// ── Reflection ─────────────────────────────────────────────────────────────
async function runReflection() {
  if (AgentState.isLoading) return;
  AgentState.isLoading = true;
  setAgentBadge('loading');
  renderReflectionSkeleton();

  try {
    const plan = AgentState.weeklyPlan?.weekly_plan || {};
    const history = buildHistory();

    const data = await fetchAgentReflect(plan, history);

    if (data.success) {
      saveReflection(data);
      renderReflection(data);
      if (typeof showToast === 'function') showToast('📊 Weekly reflection complete!', 'success');
    } else {
      throw new Error('Invalid reflection response');
    }
  } catch (err) {
    console.error('[Agent] Reflection failed:', err.message);
    if (typeof showToast === 'function') showToast('Reflection failed. Please try again.', 'error');
  } finally {
    AgentState.isLoading = false;
    setAgentBadge('online');
  }
}

// ── Renderers ──────────────────────────────────────────────────────────────
function getDayName() {
  return ['Sunday', 'Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday'][new Date().getDay()];
}

function renderWeeklyPlan(data) {
  const plan = data.weekly_plan;
  const container = document.getElementById('agent-plan-container');
  if (!container) return;

  const today = getDayName();
  const workoutCount = plan.weekly_targets?.workouts || 5;
  const avgCal = plan.weekly_targets?.avg_daily_calories || 2000;
  const proteinG = plan.weekly_targets?.protein_g || 150;

  let html = '';

  // Buddy message bubble
  if (data.user_message) {
    html += `
      <div class="plan-msg-bubble fade-up">
        <span class="buddy-icon">🤖</span>
        <span>${escHtml(data.user_message)}</span>
      </div>`;
  }

  // Summary chips
  html += `
    <div class="plan-summary-bar">
      <div class="plan-summary-chip">🎯 Goal: <strong>${escHtml(plan.goal || 'Fitness')}</strong></div>
      <div class="plan-summary-chip">💪 <strong>${workoutCount}</strong> workouts/week</div>
      <div class="plan-summary-chip">🔥 <strong>${avgCal}</strong> kcal/day</div>
      <div class="plan-summary-chip">🥩 <strong>${proteinG}g</strong> protein target</div>
    </div>`;

  // Day cards scroll
  html += '<div class="weekly-plan-scroll">';
  (plan.days || []).forEach(day => {
    const isToday = day.day === today;
    const isRest = day.workout?.type === 'rest';
    const cardClass = isToday ? 'day-card today' : isRest ? 'day-card rest' : 'day-card';
    const badgeClass = isToday ? 'day-badge today-b' : isRest ? 'day-badge rest-b' : 'day-badge workout';
    const badgeText = isToday ? 'Today' : isRest ? 'Rest' : '💪';
    const workoutIcon = isRest ? '🧘' : '🏋️';
    const durText = day.workout?.duration_min > 0 ? `${day.workout.duration_min} min · ~${day.workout.calories_burn} kcal` : 'Active recovery';

    html += `
      <div class="${cardClass}">
        <div class="day-card-header">
          <span class="day-name">${escHtml(day.day)}</span>
          <span class="${badgeClass}">${badgeText}</span>
        </div>
        <div class="day-focus">${workoutIcon} ${escHtml(day.focus || day.workout?.type || 'Rest')}</div>
        <div class="day-workout-info">⚡ ${escHtml(durText)}</div>
        <div class="day-cal-info">🍽️ ${escHtml(String(day.meals?.total_kcal || avgCal))} kcal</div>
        <div class="day-tip">${escHtml(day.tip || '')}</div>
      </div>`;
  });
  html += '</div>';

  container.innerHTML = html;

  // Animate cards in
  container.querySelectorAll('.day-card').forEach((el, i) => {
    el.style.opacity = '0';
    el.style.transform = 'translateY(12px)';
    setTimeout(() => {
      el.style.transition = 'opacity 0.3s ease, transform 0.3s ease';
      el.style.opacity = '1';
      el.style.transform = 'translateY(0)';
    }, i * 60);
  });
}

